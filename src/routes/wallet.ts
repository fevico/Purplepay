import { Router } from "express";
import { bankTransfer, createWallet, fetchBankList, getAccountDetails, walletDetails, walletTransactions } from "src/controller/wallet";
import { isAuth } from "src/middleware/auth";

const walletRouter = Router()

walletRouter.post('/create', isAuth, createWallet)
walletRouter.get('/wallet-details', isAuth, walletDetails)
walletRouter.get('/wallet-transations', isAuth, walletTransactions)
walletRouter.get('/bank-list', isAuth, fetchBankList)
walletRouter.get('/account-details', isAuth, getAccountDetails)
walletRouter.post('/bank-transfer', isAuth, bankTransfer)


